/**
 * Get cleaned topic name
 */
export function getCleanTopicName(originalName: string): string {
  // Check if we have a mapping
  if (TOPIC_MAPPINGS[originalName]) {
    return TOPIC_MAPPINGS[originalName];
  }
  
  // Fallback: clean the name
  return originalName
    .replace(/\.(pdf|docx|pptx)$/i, '')
    .replace(/[-_]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}
